package com.bnp.cservices.utils;

/**
 * Indicates the serial version UID for the serializable classes.
 * Increments this version every time a new version is created.
 */
public class SerialVersionUID {

    /**
     * The current version UID.
     */
    public static final long serialVersionUID = 1L;


    /**
     * Private because this class can not be instanciated.
     */
    private SerialVersionUID() {
        //Do nothing.
    }

}
